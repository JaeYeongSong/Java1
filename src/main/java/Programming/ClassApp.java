package Programming;

public class ClassApp {
    public static void main(String[] args) {
        // Class는 연관된 변수들이나 메소드를 모아서 Tagging 해놓은 것
        System.out.println(Math.PI); // Math라는 Class 안에 PI라는 변수
        System.out.println(Math.floor(1.6)); // Math라는 Class 안에 floor이라는 매소드
        System.out.println(Math.ceil(1.6)); // Math라는 Class 안에 ceil이라는 매소드
    }
}
